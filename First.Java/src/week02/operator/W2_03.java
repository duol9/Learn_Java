package week02.operator;

public class W2_03 {
    public static void main(String[] args) {
        // 비교 week02.연산자
        System.out.println(10 > 9); // true
        System.out.println(10 >= 9); // true
        System.out.println(10 < 9); // false
        System.out.println(10 <= 9); // false
        System.out.println(10 <= 9); // false
        System.out.println(10 == 10); // true
        System.out.println(10 == 9); // false
        System.out.println(10 != 10); // false
        System.out.println(10 != 9); // true
    }
}
